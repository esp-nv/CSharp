//Напишете програма, която чете градуси по скалата на Целзий (°C) и ги
//    преобразува до градуси по скалата на Фаренхайт (°F). Потърсете в Интернет подходяща формула,
//    с която да извършите изчисленията.
//    Форматирате изхода до втория знак след десетичната запетая. 
// C = (°F – 32) / 1,8 
//℉ = ℃ * 1.8000. + 32.00

//input
double c = double.Parse(Console.ReadLine());

//calulate
double f = (c * 1.8) + 32.00;

//output
Console.WriteLine($"{f:f2}");